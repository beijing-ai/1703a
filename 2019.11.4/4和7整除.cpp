# include <iostream>
using namespace std;
//随机输入10个1-1000之内的整数，放在数组array中，判断数组内元素是否能同时被4和7整除，若能同时整除，则输出该数组元素和下标。

int main()
{
//(1)	正确定义变量，数组（2分）
	int a[10], i;
//(2)	输入整数到数组中（3分)
	for(i=0;i<10;i++)
	{
		cin >> a[i];
//	(3)	判断数组元素能否同时被4和7整除  （6分）
		if(a[i]%4==0&&a[i]%7==0)
		{
//(4)	输出能被4和7同时整除的数组元素和其下标 （4分)
			cout << a[i];
			cout << "下标为" << i;	
		}	
	}
}
   
